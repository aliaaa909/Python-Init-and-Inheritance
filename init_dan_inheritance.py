"""**Inheritance**"""

class Mahasiswa:
  status = 'mahasiswa'

  def __init__ (self, nama, kelas):
    self.nama = nama
    self.kelas = kelas

  def keterangan(self):
    print('{} di kelas {} adalah seorang {}'.format(self.nama,self.kelas,self.status))

Joko = Mahasiswa('joko',12)

Joko.keterangan()

"""Fungsi super berfungsi untuk mencari data dari seluruh line di program python
dideklarasikan sbg super.__init__
___________________

pada class mahasiswa, data nilai dan kelas masih bisa kita lihat di fungsi init, makanya bisa dipanggil tanpa super, tapi kalau programnya panjang, kita bisa tinggal pake super tanpa nginget nama methodnya

"""

class Nilai(Mahasiswa):

    def __init__ (self, nama, kelas):
      #Mahasiswa.__init__(nama,kelas)
      super().__init__(nama,kelas)
      self.nilai_baru = []

    def input_nilai(self, tambah):
      return self.nilai_baru.append(tambah)

budi = Nilai('Budi',13)

budi.keterangan()

budi.input_nilai(80)

budi.nilai_baru