class Kulkas:

  #class object attribute (attribute yang bersifat global untuk class kulkas)
  penjual = 'pak mamat'

  def __init__(self,harga, merek):
    self.harga = harga
    self.merek = merek
    self.garansi = 3
    self.tenaga = 'listrik'

  def keterangan(self, anak = 5 ):
    print(' kulkas merek {} harganya sebesar {} dan nama penjualnya {} yang punya {} anak'.format(self.merek,self.harga,self.penjual,anak))

barang = Kulkas(200, 'LG')

